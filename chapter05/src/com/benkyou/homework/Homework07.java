package com.benkyou.homework;

public class Homework07 {
    public static void main(String[] args) {
        for (int i = 'a'; i <= 'z'; i++) {
            System.out.print((char) i);
        }
        System.out.println();
        for (int i = 'Z'; i >= 'A'; i--) {
            System.out.print((char) i);
        }
    }
}
